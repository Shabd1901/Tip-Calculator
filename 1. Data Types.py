#subscripting

print("Hello"[4])      #---> This will print the character at index 4 of the string "Hello", which is 'o'.
print("Hello"[-1])     #---> This will print the last character of the string "Hello", which is also 'o'.

#String
print("123" + "456")   #---> This will concatenate the two strings "123" and "456", resulting in "123456".

#Integer = Whole numbers
print(123 + 345)      #---> This will add the two integers 123 and 345, resulting in 468.

#Large Integers
print(1_234_567_890)  #---> This will print the large integer as is, Python can handle large integers natively.

#Float = Floating point numbers
print(3.14159)

#Boolean = True or False
print(True)          #---> This will print the boolean value True.
print(False)         #---> This will print the boolean value False.

